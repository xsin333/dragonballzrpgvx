        end
        pursuit_shot(1, 0.2, 0.0, 4500, true, 2, 0) if @cnt == 15 # 30フレーム目に攻撃
        nway_shot(1, 0.2, 0.0, 6500, true, 2, 0) if @cnt == 30 && rand(4) == 1# 30フレーム目に攻撃
        pursuit_shot(1, 0.2, 0.0, 4500, true, 2, 0) if @cnt == 50 # 30フレーム目に攻撃
        Audio.se_play("Audio/SE/"+TBOSS::SE_NAME[5]) if @cnt == 35
      end  
    else
    @vy = 0
    @vx = 1500
    
    end
    #elsif @state == 1   # 状態１：左側へ大ジャンプ
    #  if self.x <= 64 # 左端まで移動したら
    #    @state = 2  # 次の状態へ移行
    #    @cnt = 0    # カウンタリセット
    #    @vx = 0     # 移動速度をセット
    #    @dir = 6    # 右を向く
    #  end
      #jump(-4096)
    #elsif @state == 2   # 状態２：左側で待機
    #  if @cnt == 90 # 90フレーム経過したら
    #    @state = 3  # 次の状態へ移行
    #    @cnt = 0    # カウンタリセット
    #    @vx = 8192  # 移動速度をセット
    #  end
    #  nway_shot(1, 0.2, 0.0, 5500, true, 2, 1) if @cnt == 30 # 30フレーム目に攻撃
    #  nway_shot(1, 0.0, 0.0, 5500, true, 1, 1) if @cnt == 40 # 40フレーム目に攻撃
    #elsif @state == 3   # 状態３：右側へダッシュ
