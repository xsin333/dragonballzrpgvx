          @vy = 1500
        elsif $shoot_player_y < $shoot_enemy_y
          @vy = -1500
        else
          @vy = 0
        end
        if @cnt >= 60 # 90フレーム経過したら
          @cnt = 0    # カウンタリセット
        end
        nway_shot(1, 0.2, 0.0, 4500, true, 2, 0) if @cnt == 15 # 30フレーム目に攻撃
        nway_shot(1, 0.2, 0.0, 4500, true, 2, 0) if @cnt == 50 # 30フレーム目に攻撃
        Audio.se_play("Audio/SE/"+TBOSS::SE_NAME[5]) if @cnt == 35
