        if $shoot_player_y > $shoot_enemy_y
          @vy = 2000
        elsif $shoot_player_y < $shoot_enemy_y
          @vy = -2000
        else
          @vy = 0
        end
        if @cnt >= 60 # 90フレーム経過したら
